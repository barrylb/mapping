create table tmp1 as
select ST_Collect(geom) as geom, elect_div, state_pid, ce_pid
from comm_electoral
where elect_div='BONNER'
group by elect_div,state_pid,ce_pid;

update comm_electoral set elect_div='BONNER2'
where elect_div='BONNER';

create table tmp2 as
SELECT ST_Intersection(geom, ST_PolygonFromText('POLYGON((153.2058226990996 -27.29903235255222,153.1535163784751 -27.39152472216038,153.1584544557673 -27.39811467980375,153.17188029572 -27.39874742429342,153.2326333966319 -27.44361540045504,153.2933895371179 -27.39498832403109,153.2806468724725 -27.32506168655202,153.2058226990996 -27.29903235255222))')) as geom
from ste_2011_aust
where
ST_AsText(ST_Intersection(geom, ST_PolygonFromText('POLYGON((153.2058226990996 -27.29903235255222,153.1535163784751 -27.39152472216038,153.1584544557673 -27.39811467980375,153.17188029572 -27.39874742429342,153.2326333966319 -27.44361540045504,153.2933895371179 -27.39498832403109,153.2806468724725 -27.32506168655202,153.2058226990996 -27.29903235255222))'))) != 'GEOMETRYCOLLECTION EMPTY';

insert into comm_electoral (ce_pid, state_pid, elect_div, geom)
SELECT 'QLD2', '3', 'BONNER', ST_Union(tmp1.geom, tmp2.geom)
from tmp1, tmp2;